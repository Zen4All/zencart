<?php

namespace Tests\FeatureStore\TaxCalculationsSEK\TestBasicTaxCalculationsWithShippingTax;

use Symfony\Component\DomCrawler\Crawler;
use Tests\Support\zcFeatureTestCaseStore;

class BasicTaxShippingBillingWithShippingSEKTest extends zcFeatureTestCaseStore
{
    private static $ready = false;
    public function setUp(): void
    {
       parent::setUp(); // TODO: Change the autogenerated stub
        if (static::$ready) {
            return;
        }
        static::$ready = true;
        $this->setConfiguration('MODULE_SHIPPING_ITEM_TAX_CLASS', 1);
        $this->setConfiguration('DEFAULT_CURRENCY', 'SEK');
    }

    /**
     * @test
     * scenario BTC SEK 2
     */
    public function testBasicCheckoutFloridaCustomer()
    {
        $this->createCustomerAccountOrLogin('florida-basic1');
        $this->browser->request('GET', HTTP_SERVER . '/index.php?main_page=product_info&products_id=25');
        $response = $this->browser->getResponse();
        $this->assertStringContainsString('Microsoft', (string)$response->getContent() );
        $this->browser->submitForm('Add to Cart', [
            'cart_quantity' => '1',
            'products_id' => '25',
            ]);
        $response = $this->browser->getResponse();
        $this->assertStringContainsString('Your Shopping Cart Contents', (string)$response->getContent() );
        $this->browser->request('GET', HTTP_SERVER . '/index.php?main_page=checkout_shipping');
        $response = $this->browser->getResponse();
        $this->assertStringContainsString('Delivery Information', (string)$response->getContent() );
        $this->browser->submitForm('Continue', [
        ]);
        $response = $this->browser->getResponse();
        $this->assertStringContainsString('Payment Information', (string)$response->getContent() );
        $crawler = new Crawler((string)$response->getContent());
        $this->assertStringContainsString(\PHP_VERSION_ID < 80400 ? '12,2483' : '12,2482', $crawler->filter('#checkoutOrderTotals')->text());
        $this->assertStringContainsString('0,4375', $crawler->filter('#checkoutOrderTotals')->text());
        $this->assertStringContainsString('0,8880', $crawler->filter('#checkoutOrderTotals')->text());
        $this->assertStringContainsString('SEK13,5738SEK', $crawler->filter('#checkoutOrderTotals')->text());
        $this->browser->submitForm('Continue', []);
        $response = $this->browser->getResponse();
        $crawler = new Crawler((string)$response->getContent());
        $this->assertStringContainsString('Order Confirmation', $crawler->filter('#checkoutConfirmDefaultHeading')->text());
        $this->assertStringContainsString(\PHP_VERSION_ID < 80400 ? '12,2483' : '12,2482', $crawler->filter('#orderTotals')->text());
        $this->assertStringContainsString('0,4375', $crawler->filter('#orderTotals')->text());
        $this->assertStringContainsString('0,8880', $crawler->filter('#orderTotals')->text());
        $this->assertStringContainsString('SEK13,5738SEK', $crawler->filter('#orderTotals')->text());
        $this->browser->submitForm('btn_submit_x', []);
        $response = $this->browser->getResponse();
        $crawler = new Crawler((string)$response->getContent());
        $this->assertStringContainsString('Your Order Number is:', $crawler->filter('#checkoutSuccessOrderNumber')->text());
    }

